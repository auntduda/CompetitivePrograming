#include <bits/stdc++.h>
 
#define pb push_back
#define mp make_pair
#define pii pair<int, int>
#define ff first
#define ss second
#define ll long long
#define int long long

using namespace std;

const ll MAX = 1e7+17;
const ll INF = 1e9+17;

int32_t main() {
    ios::sync_with_stdio(false);
    cin.tie(NULL);

    ll n, k; cin >> n >> k;

    ll a = 0;
    for (ll i = 0; i <= MAX; i++) {
        if (i * i == k) {
            a = i;
            break;
        }
    }
    
    ll x = 0;
    if (a > n) {
        ll aux = a - n;
        aux *= 2;
        aux--;

        x = k - aux * (2 * n + 1); 
    } else {
        ll aux = n - a;
        aux *= 2;
        aux++;

        x = k + aux * (2 * n + 1);
    }

    cout << x << '\n';
   
    return 0;
}